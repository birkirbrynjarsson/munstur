               <h3>Verkefnið Altarisdúkar</h3>
           </div>
           <div class="entry-content">

           	   <p><a href="/images/altarisdukar/seria/1.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/1-tn.jpg" alt="" /></a></p>

               <p>Hugmyndin að verkefninu Altarisdúkar kviknaði á útmánuðum árið 2003 út frá vinnu við textílhandverk og áhuga á munstrum. Þá höfðu íslenskir altarisdúkar, okkur vitanlega, ekki áður verið teknir sérstaklega til skoðunar og er verkefnið Altarisdúkar því frumkvöðlaverkefni. Verkefnið fór hægt af stað og tekinn var tími í undirbúning svo sem að móta markmið og vinnulag. Fljótlega var ákveðið að taka aðeins fyrir afmarkað svæði í einu, eitt prófastsdæmi, og ljúka við að skoða alla altarisdúka þar og ganga frá efni sem safnast hefur í tölvutæku formi. Einnig að stefna að því að vinna á hverju svæði endaði með kynningu/sýningu á gögnum úr verkefninu í samvinnu við prófastdæmin og söfn í héraði. Vinnan hófst í Eyjafirði og síðan hafa kirkjur víða um landið verið heimsóttar.</p>

               <p><strong>Jenný Karlsdóttir</strong> og <strong>Oddný E. Magnúsdóttir</strong> hafa unnið saman að verkefninu frá upphafi og stefna að því að halda því áfram sem víðast um landið, helst að loka hringnum.</p>

               <p>Helsta viðfangsefni og markmið verkefnisins eru:</p>
               	<ul>
               		<li>Að kanna munstur og gerð altarisdúka.</li>
			   		<li>Að ljósmynda dúkana og skrá upplýsingar.</li>
               	</ul>			

                <p><a href="/images/altarisdukar/seria/6.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/6-tn.jpg" alt="" /></a></p>

                <h3>2016</h3>
                <p>Árið 2016 var lokið við að skoða dúka í Borgarfirði og á Mýrum.</p>

                <h3>2015</h3>
                <p>Haustið 2015 var farin vinnuferð austur á land og í nóvember það ár lauk verkefninu í Árnesþingi með sýningu í Húsinu á Eyrarbakka í samvinnu við Byggðasafn Árnesinga.</p>

                <h3>2014</h3>
				<p>Það ár voru farnar vinnuferðir um Borgarfjörð, Mýrar og Suðurland (Árnesþing) og heimsóttar kirkjur á þeim svæðum.</p>

                <p><a href="/images/altarisdukar/seria/2.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/2-tn.jpg" alt="" /></a></p>

                <h3>2013</h3>
				<p>Á haustdögum það ár var farin fyrsta ferðin í Austurlandsprófastsdæmi og heimsóttar nokkrar kirkjur á Héraði, Skriðdal, Fljótsdal og Jökuldal.</p>

                <p><a href="/images/altarisdukar/seria/3.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/3-tn.jpg" alt="" /></a></p>

                <h3>2012</h3>
                <p>Árið 2012 voru heimsóttar allar kirkjur í Hólmavíkurprestakalli og vinnu lokið í ágúst en ekki reyndist unnt að hafa þar kynningu eða sýningu.</p>

                <p><a href="/images/altarisdukar/seria/4.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/4-tn.jpg" alt="" /></a></p>

                <h3>2010-2011</h3>
                <p>Unnið var að verkefninu í Húnavatnssýslum sumarið 2010, með stuðningi Menningarráðs Norðurlands vestra og lauk henni þar í júní 2011. Þá var sett upp sýning í Klausturstofu á Þingeyrum í samvinnu við Byggðasafn Húnvetninga og Strandamanna.</p>

                <p><a href="/images/altarisdukar/seria/5.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/5-tn.jpg" alt="" /></a></p>

                <h3>2009-2010</h3>
                <p>Vinna hófst í Skagafirði 2009 og lauk henni með sýningu á Löngumýri í samstarfi við prófastsdæmið. Sýningin stóð til hausts á Löngumýri.</p>

                <h3>2009-2010</h3>
				<p>Ein ný afurð varð til í verkefninu Altarisdúkar á árinu 2009, með stuðningi Atvinnumálasjóðs kvenna, en það var dagatal (fyrir árið 2010) með ljósmyndum af munstrum dúka og kirkjum, sýnishorn úr því efni sem aflast hafði þá. Grafísk hönnun var unnin af Stíl í samvinnu við okkur og var dagatalið til sölu í Kirkjuhúsinu í Reykjavík og víðar.</p>

                <p><a href="/images/altarisdukar/dagatal2.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/dagatal2-tn.jpg" alt="" /></a></p>

                <h3>2009</h3>
				<p>Árið 2009 bað Kirkjumiðstöðin Kirkjubær á Akureyri um myndir og önnur gögn verkefnisins úr Eyjafjarðarprófastsdæmi til sýningar í húsnæði sínu og var sýningin og verkefnið kynnt þar af undirrituðum 3. apríl um vorið.</p>
				<p>Sama ár (2009) barst beiðni frá Glerárkirkju um sýningu á gögnum verkefnisins og var hún sett upp í anddyri kirkjunnar í desember það ár.</p>

                <p><a href="/images/altarisdukar/seria/8.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/8-tn.jpg" alt="" /></a></p>

                <h3>2007-2008</h3>
                <p>Unnið var að verkefninu og allar kirkjur í Þingeyjarsýslum heimsóttar sumurin 2007 og 2008, og í lok vinnu var sýning haldin í Safnahúsinu á Húsavík og Byggðasafninu á Snartarstöðum í september 2008. Voru sýndar ljósmyndir af altarisdúkum og kirkjum í möppum og á veggjum en einnig í skjávarpa. Eldri dúkar í eigu safnanna voru auk þess til sýnis.</p>

                <p><a href="/images/altarisdukar/seria/9.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/9-tn.jpg" alt="" /></a></p>

                <h3>2007</h3>
				<p>Gögn úr verkefninu voru sýnd á Kirkjulistaviku í Akureyrarkirkju 28. apríl -6. maí 2007</p>

                <p><a href="/images/altarisdukar/seria/10.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/10-tn.jpg" alt="" /></a></p>

				<h3>2006</h3>
				<p>Fyrsta svæðið sem tekið var fyrir var Eyjafjarðarprófastsdæmi en vinnu lauk þar með sýningu í samstarfi við Minjasafnið á Akureyri og var sýningin sett upp þar. Á sýningunni voru vandaðar sérhannaðar möppur með myndum af öllum dúkum sem til náðist í prófastsdæminu og fróðleik um dúkana en einnig stækkaðar myndir af kirkjunum. Þá voru til sýnis gamlir altarisdúkar úr eigu safnsins.</p>

                <p><a href="/images/altarisdukar/seria/11.jpg" rel="prettyPhoto[altarisdukar]"><img class="aligncenter" src="/images/altarisdukar/seria/11-tn.jpg" alt="" /></a></p>

