                <h3 style="padding-top:1em;">Styrkir sem verkefnið hefur fengið</h3>
				<p>Verkefnið Altarisdúkar hefur frá því það hófst hlotið styrki frá ýmsum menningar- og styrktarsjóðum á landinu sem nægt hafa til að standa straum af kostnaði, svo sem við ljósmyndun, ferðakostnað, prentun ofl. Þar á meðal eru Kristnihátíðarsjóður, sem fleytti verkefninu af stað í upphafi, Þjóðhátíðarsjóður, Atvinnumálasjóður kvenna, Langamýri, Menningar- og listasjóður Þingeyinga, Menningarráð Norðurlands vestra, Kirkjumálasjóður, Menningarráð Suðurlands og fleiri sjóðir. Fyrir það allt erum við sem að verkefninu stöndum afar þakklátar.</p>


