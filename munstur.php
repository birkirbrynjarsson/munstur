            <p>En munsturútgáfan hefur hlaðið utaná sig eins og snjóbolti. Titlarnir eru orðnir nokkuð margir. Annars vegar hef ég endurútgefið gamlar íslenskar handverksbækur sem líkastar því sem þær voru og bætt við smá formála með upplýsingum um höfundinn. Hins vegar eru það gömul munstur, gjarnan sitt úr hverri áttinni, sem ég hef raðað saman í hefti eftir efni t.d. milliverk, útskurður og fl.</p>
            <p>Útgáfan er ekki stór í sniðum hjá mér eða umsetningin mikil og þannig vil ég hafa það. Þetta er áhugamál fyrst og fremst og meðan mér þykir þetta gaman og einhverjir vilja eignast bækurnar mínar langar mig að halda áfram.<br/>Áhugasamir geta haft samband við mig í síma 860-4933 eða á netfangið jenny@munstur.is</p>

