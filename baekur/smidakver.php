           <h2><a href="#">Lítið smíðakver</a></h2>
           
           
           <div class="entry-content">
<h4>samið hefur Pjétur Pjétursson (1886).</h4>
<p><a href="/images/baekur/smidakver-1.jpg" rel="prettyPhoto[smidakver]"<img class="aligncenter" src="../images/baekur/smidakver-1_tn.jpg" alt="" /></a></p>
<h3>Um höfundinn</h3>

<p>Pétur Jökull Pétursson fæddist á Hákonarstöðum á Jökuldal</p>

<p>28. okt. 1828.  Foreldrar hans voru Pétur Pétursson og  Ingibjörg Pálsdóttir.</p>

<p>Pétur var bráðþroska og snemma bar á óvenjulegu hugviti hans og gáfum einkum er laut að smíði, myndgerð eða dráttlist. Fyrsta smíðisgripinn gerði hann á þrettánda ári en það var lóðavog. Hins vegar varð honum ráðafátt þegar kom að því að merkja hana. Fékk hann þá vin sinn og nágranna Snorra Guðmundsson í Klausturseli sér til hjálpar og vannst  verkið þá fljótt og vel.</p>

<p>Litla menntun hlaut hann í föðurhúsum aðra en að lesa lærdómskverið og lítilsháttar að draga til stafs. Þegar hann var á sautjánda ári bað hann föður sinn sem var stórríkur um fararefni til siglinga og kvaðst vilja læra að smíða eða mennta sig á annan hátt. En ekki var við það komandi. Eftir þessa neitun ákvað hann að reyna að mennta sig sjálfur.  Ferðaðist hann á milli smiða og aflaði sér allrar þeirrar þekkingar sem mögulegt var að fá. Vann við smíðar heima og lagði gjörva hönd á margt og var duglegur og eftirsóttur til vinnu.</p>

<p>Pétur varð snemma listaskrifari og iðulega fenginn til að kenna börnum að draga til stafs og mjög áhugasamur um að kenna unglingum það sem þeir vildu læra.</p>

<p>Hákonarstaðamenn höfðu undir höndum Galdrabók séra Einars á Skinnastað. Þegar Pétur var á 18. ári notaði hann hverja stund um veturinn og afritaði Galdrabókina sem er 377 bls. og lauk því verki um páska.</p>

<p>Pétur eignaðist barn, Sigfús fæddan 23.febrúar 1848  með vinnukonu sem var á Hákonarstöðum. Barnsmóðir hans hét Oddný Sigurðardóttir. Sigfús Pétursson ólst upp á Hákonarstöðum og síðar hjá Sigfinni föðurbróður sínum.  Sigfús lést              15. september 1870. Oddný átti síðar barn með Vigfúsi bróður Péturs Jökuls.</p>

<p>Árið 1851 kvæntist  hann Guðrúnu Jónsdóttur frá  Haugsstöðum á Jökuldal. Þau bjuggu á Hákonarstöðum næstu 10 árin og fékkst Pétur við smíðar samhliða búskapnum. Þá slitu þau samvistir og flutti Guðrún með Pétur son þeirra í Haugsstaði.</p>

<p>Mikill aldursmunur var á þeim Pétri og Guðnýju og var Guðný 15 árum eldri en Pétur.</p>

<p>Pétur stundaði áfram smíðar og fór víða um Austurland og var alls staðar kærkominn gestur því víða var nóg af verkefnum handa honum sem aðrir gátu ekki unnið.</p>

<p>Pétur skrifaði mikið og var í bréfaskriftum við menn víða um land, og var þá að leita ýmissa uppplýsinga.</p>

<p>Smíðakver þetta samdi Pétur og gaf út á Akureyri 1868</p>

<p>Má nærri geta að það hefur verið þarft verk því á þeim tíma var svo til ómögulegt að fá tilsögn í smíðum hér á landi.</p>

<p>Pétur fluttist til Vesturheims. Þar kvæntist hann Sigríði Vigfúsdóttur. Það hjónaband stóð ekki lengi.</p>

<p>Pétur hefur löngum verið þjóðsagnapersóna á Jökuldal þótti dverghagur, jafnvel göldróttur og mikill vaskleikamaður. Gekk um með hamar og meitil og víða sjást enn þá ártöl og jafnvel stafir í steinum á Jökuldal ef eftir er leitað.</p>

<p>Pétur lést Vestanhafs í lok nítjándu aldar en heimildum um dánarár hans ber ekki saman.</p>

 
<h5 align="center">Lýsing er til af Pétri í  V. árg. Óðins, 6. tbl.</h5>


