<h4>eftir Jón Bernharðsson</h4>
<p><a href="/images/baekur/smidakver-2.jpg" rel="prettyPhoto[smidakver]"<img class="aligncenter" src="../images/baekur/smidakver-2_tn.jpg" alt="" /></a></p>
<h3>Um höfundinn</h3> 

<p>Jón Bernharðsson   fæddist  4. júlí 1822 að Skógsnesi í Gaulverjabæjarhreppi.</p>

<p>Jón útskrifaðist  gullsmíðasveinn af Jóni Arngrímssyni Gullsmíða-meistara í Kaupmannahöfn  29. apríl  1850. Sveinsstykki hans var súpuskeið. Jón kom frá námi  1851 og settist að í Reykjavík.</p>

<p>Hann fékk borgararéttindi  10. nóv.  1852  en varð gjaldþrota sennilega vegna húsakaupa og mun þá hafa flust að Laxnesi í Mosfellssveit.</p>

<p>Kona Jóns var Margrét Bjarnadóttir bónda í Reykjadalskoti í Hrunamannahreppi.</p>

<p>Jón dó 24.mars 1877 í Víðinesi og þá kallaður bóndi þar.</p>

<p>Eftir Jón eru þekktir nokkrir  skúfhólkar í einkaeign og á söfnum.  Einnig er líklega eftir hann teskeið í Byggðasafni Snæfells og Hnappdæla. Í Hrunakirkju eru  þjónustukaleikur og patína eftir Jón. Hann mun hafa haft stimpilinn  J. B.</p>

<p>Jón samdi "Lítið smíðakver" sem Bjarni sonur hans gaf út og var prentað í Ísafoldarprentsmiðju  1887.</p>

