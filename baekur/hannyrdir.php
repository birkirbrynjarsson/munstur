<h3>Formáli</h3>
<p>Árið 1886 kom út fyrsta íslenska hannyrðabókin.</p>

<p>Þrjár merkiskonur stóðu að útgáfu þessari.  Þær voru: Þóra Pétursdóttir Thoroddsen (1848-1917), Jarðþrúður Jónsdóttir (1851-1924) og Þóra Jónsdóttir Magnússonar (1858-1947).  Allar voru þær mjög vel menntaðar að þeirrar tíðar hætti.  Höfðu allar dvalið og menntast erlendis og þannig víkkað sjóndeildarhring sinn, auk þess sem þær komu frá einhverjum mestu menningarheimilum sem þá fyrirfundust hér á landi.  Þær giftust mektarmönnum og voru húsfreyjur á umsvifamiklum heimilum.  Það er til að halda minningu þessara brautryðjenda á lofti og áhuga þeirra á handverki, að ég ræðst í endurútgáfu á þessari hannyrðabók.</p>

<p>Ég tel að okkur Íslendingum beri skylda til að varðveita þá menningararfleifð sem við höfum þegið og skila þeim arfi í hendur komandi kynslóða. </p>

