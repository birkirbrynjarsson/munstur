<h3>Formáli</h3>
<p>Elísabet Ingveldur Valdimarsdóttir fæddist í Hnífsdal 26. febrúar 1890 og lést 7. júlí 1973 í Reykjavík.</p> 

<p>Hún fór ung til Danmerkur á hannyrðaskóla sem rekinn var af Den danske Kunstflidsforening.  Þarna lærði hún ýmis konar hannyrðir sem og fatasaum.  Heim komin settist hún að á Ísafirði og tók að halda námskeið fyrir konur.  Kenndi hún þar ýmsar hannyrðalistir, en einkum kenndi hún konum allt er laut að gerð íslensku kvenbúninganna, bæði að baldyra og upphlutsborða, knipla, orkéra og vinna allt er tilheyrði búningagerðinni.  Námskeið þessi voru afar vinsæl og voru frá sjö og upp í fjórtán konur á hverju námskeiði.  Síðar fluttist Elísabet til Reykjavíkur og setti upp fatahreinsun á Vesturgötu.  Eftir stríð keypti hún bakhúsið að Laugavegi 47 ásamt Jónu Helgu tvíburasystur sinni.  Þar ráku þær fataviðgerðir í kjallara hússins og tóku meðal annars að sér að kúnststoppa.</p>

<p>Ég ræðst nú í að endurútgefa bók þessa sem fyrst og fremst er kennslubók í fjölbreyttum hannyrðum en hún hefur verið ófáanleg um langt árabil.</p>

