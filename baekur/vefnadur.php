<p><a href="/images/baekur/vefnadur_utsaumur.jpg" rel="prettyPhoto[vefnadur_utsaumur]"><img class="aligncenter" src="/images/baekur/vefnadur_utsaumur_tn.jpg" alt="" /></a></p>
<h3>Formáli</h3>
<p>Halldóra Bjarnadóttir sá um útgáfu og ritstýrði HLÍN ársriti Sambands norðlenskra kvenna í 40 ár.  Með HLÍN komu út nokkur fylgirit þar á meðal þrjú hefti um “Íslenskar vefnaðar og útsaumsgerðir”</p>

<p>Halldóra Bjarnadóttir fæddist að Ási í Vatnsdal 14. október 1873 og lést á Héraðssjúkrahúsinu á Blönduósi 28. nóvember 1981 þá 108 ára gömul, elst allra Íslendinga.</p>

<p>Halldóra var skólastjóri Barnaskóla Akureyrar frá 1908-1918 og fyrsta íslenska konan sem stjórnaði stórum barnaskóla hér á landi.  Halldóra var ráðunautur Búnaðarfélags Íslands í heimilisiðnaðarmálum í hartnær 30 ár.  Hún stofnaði tóvinnuskóla á Svalbarði í Eyjafirði og starfrækti hann um nokkurt skeið (1946-1955).</p>

<p>Halldóra var brautryðjandi í félags- og menntunarmálum kvenna og lét ekkert tækifæri ónotað til að brýna konur til að efla samtakamátt sinn og hvetja þær til dáða.  Halldóru var það mikið metnaðarmál að fornar verkhefðir féllu ekki í gleymsku og dá og hún treysti íslenskum konum til þess að flytja verkkunnáttu frá einni kynslóð til annarrar.  Halldóra vann ötullega að söfnun gamalla handverksmuna, einkum á sviði tóskapar og vefnaðar.  Safnaði hún gömlum munum víðsvegar um landið og er afrakstur þeirrar söfnunar varðveittur í Halldórustofu í  Byggðasafninu á Blönduósi.  Hiklaust má fullyrða að áhrifa Halldóru Bjarnadóttur í því að hefja íslenskan vefnað og tóvinnu til vegs og virðingar meðal íslensku þjóðarinnar gæti enn í dag.</p>

 

<p>Þessi þrjú rit um “Íslenskar vefnaðar og útsaumsgerðir” sem komu út sem fylgirit HLÍNAR. Það gefur heftunum ákveðið gildi að Halldóra lætur þess getið við flest munstrin hvaðan  hún fékk þau. Ég hef ráðist í endurútgáfu á ritum þessum í þeirri trú að handverksfólk um allt land taki þátt í því að flytja forna handverksmenningu frá fortíð til framtíðar.</p>

