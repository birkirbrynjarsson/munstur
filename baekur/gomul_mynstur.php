<h4>Samantekt Jenný Karlsdóttir.</h4>

<p>Í heftum 1 og 3 eru reitamunstur (krosssaumsmunstur) sem líka er hægt að hekla eftir t.d. myndir í glugga, dúka og fleira.                           Einnig  eru í hefti 3 nokkur gardínumunstur.<br/>
Í hefti 2 eru líka reitamunstur fyrir krosssaum og gjarnan með litaskýringum. Einnig  rósapúðar og munstur af árstíðunum.
Í 4. heftinu eru aftur á móti munstur fyrir fjölbreyttari útsaumsgerðir,  t.d.  kontórsting , flatsaum og lykkjuspor og jafnvel kúnstbróderi. Einnig eru í heftinu munstur í sængur og koddaver.</p>
<p>Ég er oft spurð um ,,íslensk munstur"  en erfitt er að tala um landamæri þegar munstur eru annars vegar.                                                                                                               Gaman er að segja frá því að 1997 þegar ég setti saman efni í hefti 1 og 2 tók ég ýmislegt úr Nýju Kvennablaði sem kom út            1940-1960. Síðan hef ég fundið þessi sömu munstur í gömlum dönskum Familie Jurnal -blöðum frá 1920.                                               Þarna sjáum við söguna í hnotskurn, arf kynslóðanna mann fram af manni. 
</p>


