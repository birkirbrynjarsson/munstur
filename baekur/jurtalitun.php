<h4>Eftir Kristínu Þorsteinsdóttur. (1942)</h4>

<p>Kristín fæddist í Hólakoti í Ólafsfirði 14. okt.1892  dóttir hjónanna Þorsteins Jónssonar og Sigurbjargar Jóhannesdóttur.  Hún andaðist í Kaupmannahöfn 21. ág. 1962.</p>

<p>Snemma komu listrænir hæfileikar Kristínar í ljós.  Hún var fróðleiksfús og þráði menntun.  Fór hún til Danmerkur og Noregs og lagði þar stund á fjölbreyttar handmenntir þ.á.m. vefnað,  jurtalitun, balderingu og útskurð.</p>

<p>Eftir heimkomuna giftist hún Þorvaldi Sigurðssyni útgerðarmanni og sparisjóðsstjóra í Ólafsfirði. Eignuðust þau eina dóttur, Sigurbjörgu og tóku í fóstur Jakobínu Jónsdóttur  Heimili þeirra var þekkt fyrir gestrisni og höfðingsbrag.  Það má leiða að því getum að húsfreyju á svo umsvifamiklu heimili hafi ekki alltaf gefist mikið tóm til að sinna áhugamálum sínum við hannyrðir og jurtalitun.</p>

<p>Á stóru Heimilisiðnaðarsýningunni 1930 þar sem sýnt var úrval handmennta af öllu landinu vakti sálmabók sem Kristín hafði balderað utanum mikla athygli.  Bók þessi þótti svo mikil gersemi að í framhaldi af því, var hún send á heimssýninguna í New York.</p>

<p>Kristín jurtalitaði  band um árabil fyrir skrifstofuna "Íslensk Ull" sem var umboðsaðili fyrir sölu á íslensku bandi og ullarvörum.  Laufey Vilhjálmsdóttir veitti skrifstofunni forstöðu.</p>

<p>Fyrir áeggjan Laufeyjar samdi Kristín bókarkver þetta með leiðbeiningum um jurtalitun. Skrifstofan "Íslensk Ull" gaf kverið út 1942.</p>

