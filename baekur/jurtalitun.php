<h4>Eftir Matthildi Halldórsdóttur. (1944)</h4>
<h3>Aðfararorð</h3>

<p>Jurtalitun á sér langa sögu og hefur verið stunduð hér allt frá landnámi.  Hún var þáttur í verkhefð okkar öldum saman.  Nægir í því sambandi að nefna lýsingar á litklæðum fornkappa.  Það töldust hlunnindi að eiga litunargrös í landareign sinni.  Í Svarfdælasögu sem gerist á 10. öld segir svo: “Á þessu sama hausti sóttu þeir litgrös, bræður frá Brekku upp í Klaufahlíð, er móðir þeirra sendi þá eftir.”</p>

<p>Hér á landi hafa margar jurtalitunaraðferðir forfeðra okkar varðveist fram á okkar daga.  Ástæður þess að ég ræðst í endurútgáfu þessarar litunarbókar eru einkum þær að ég vil kynna þessa fornu verkhefð fyrir  nútímafólki.</p>

<h3>Matthildur Halldórsdóttir</h3>

<p>Matthildur Halldórsdóttir fæddist á Kálfaströnd í Mývatnssveit 23.desember 1886 (d. 11. febrúar 1974) dóttir Halldórs Sigurðssonar bónda og konu hans Hólmfríðar Þorsteinsdóttur.</p>

<p>Matthildur ólst upp á Kálfaströnd ásamt systkinum sínum sem upp komust, Halldóru, Elínu og Valdimar.  Einn vetur var hún við nám í Kvennaskólanum í Reykjavík.  Hún giftist 1913 Benedikt Baldvinssyni bónda í Garði í Aðaldal og við þann bæ var Matthildur jafnan kennd síðan.</p>

<p>Matthildur fékk snemma áhuga á jurtalitun. Hún lærði af móður sinni að lita úr birkiberki, mosa og sortulyngi, en alla ævi vann hún að tilraunum og þróun á jurtalitun.  Eins og hún segir sjálf frá þráði hún að flytja litadýrð íslenskrar náttúru inn í litsnauða tilveru hversdagsleikans.</p>

<p>Um margra ára skeið sá Matthildur hússtjórnarskólum landsins og mörgum hannyrðaverslunum fyrir jurtalituðu bandi.  Í fyrstu var einungis um heimaunnið band að ræða en eftir að var farið að framleiða band hjá ullarverksmiðju Gefjunar á Akureyri lét hún vinna þar band að sinni fyrirsögn.  Reyndust þeir Gefjunarmenn henni einstaklega vel.  Umsvifin jukust og sum árin keypti hún og litaði um 200 kg af bandi.  Hiklaust má telja Matthildi í Garði meðal merkustu brautryðjenda í jurtalitun hér á landi.</p>

<p>Árið 1944 gaf hún út leiðbeiningakver um jurtalitun.  Þessi bók hefur verið ófáanleg um áratugaskeið.</p>

<p>Í þeirri von að bók þessi nýtist þeim sem áhuga hafa á jurtalitun óska ég notendum gleði og góðs gengis.</p>

