<h4>Eftir Þórdísi Stefánsdóttur. (1919)</h4>

<p>Þórdís Stefánsdóttir fæddist á Valþjófsstað í Fljótsdal 27. október 1869 dóttir Stefáns Péturssonar prests þar og konu hans Ragnhildar Metúsalemsdóttur.</p>

<p>Hún stundaði nám við Kvennaskólann að Ytri-Ey í Húnavatnssýslu árin 1889-1891.  Var við hannyrða og tungumálanám í Reykjavík 1895-1896.  Stundaði hússtjórnar og enskunám í Bretlandi 1898-1899.  Hússtjórnarkennslunám í Bergen 1899-1900.  Kennari við Kvennaskólann á Akureyri  1896-1898 og settur skólastjóri annað árið.  Hússtjórnarskóla Reykjavíkur 1900-1901.  Kvennaskólanum á Akureyri 1901-1902.  Handavinnukennari við Barnaskóla Akureyrar 1920-1935.</p>

<p>Hún giftist árið 1901 Davíð Sigurðssyni trésmíðameistara á Akureyri og stundaði húsfreyju og kennslustörf á Akureyri um áratugaskeið.  Þórdís var alla ævi mikil hannyrðakona.  Hún var sérlega vel menntuð og hafði numið við virta skóla erlendis.  Þórdís hafði einlægan áhuga á því að vekja konur til skilnings á notagildi íslenskra jurta til litunar og því réðst hún í það fyrir tilmæli Heimilisiðnaðarfélags Íslands að semja leiðarvísi þennan. Ritið kom út sem fylgirit annars árgangs 19. JÚNÍ árið 1919 þá fyrir tilstuðlan Ingu L. Lárusdóttur.</p>

